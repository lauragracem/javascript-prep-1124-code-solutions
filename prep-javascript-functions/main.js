function subtractTwoNumbers(num1, num2) {
  return num1 - num2;
}

const subtractTwoNumbersResults = subtractTwoNumbers(9, 0);
console.log('subtractTwoNumbers exercise:', subtractTwoNumbersResults);

function getCircleCircumference(radius) {
  π = 3.14;
  return 2 * π * radius;
}

const getCircleCircumferenceResults = getCircleCircumference(3);
console.log('getCircleCircumference exercise:', getCircleCircumferenceResults);

function getFullName(firstName, lastName) {
  return firstName + ' ' + lastName;
}

const getFullNameResults = getFullName('Eleanor', 'Dela Cruz');
console.log('getFullName exercise:', getFullNameResults);

